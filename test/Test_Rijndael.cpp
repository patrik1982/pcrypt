#include "Test_Rijndael.h"

#include "BlockCipherTest.h"
#include "Rijndael.h"
#include "Test_Common.h"

using namespace std;

namespace TestRijndael {

	void TestAll() {

		BlockCipherTest t("Rijndael");

		vector<uint8_t> plaintext128 = {
			0x76, 0x2A, 0x5A, 0xB5, 0x09, 0x29, 0x18, 0x9C,
			0xEF, 0xDB, 0x99, 0x43, 0x47, 0x90, 0xAA, 0xD8
		};
		vector<uint8_t> plaintext192 = {
			0x33, 0x69, 0xEB, 0x82, 0x97, 0x36, 0x35, 0xE9,
			0xC2, 0xE9, 0x6D, 0x68, 0x77, 0x24, 0xC7, 0x90
		};
		vector<uint8_t> plaintext256 = {
			0xEA, 0xB4, 0x87, 0xE6, 0x8E, 0xC9, 0x2D, 0xB4,
			0xAC, 0x28, 0x8A, 0x24, 0x75, 0x7B, 0x02, 0x62
		};
		t.setPlaintext128(plaintext128);
		t.setPlaintext192(plaintext192);
		t.setPlaintext256(plaintext256);

		t.setCipher(new Rijndael());

		t.TestAll();
	}

}
