#include "Test_RC6.h"

#include "BlockCipherTest.h"
#include "RC6.h"
#include "Test_Common.h"

using namespace std;

namespace TestRC6 {

	void TestAll() {

		BlockCipherTest t("RC6");

		vector<uint8_t> plaintext128 = {
			0xB8, 0x4E, 0x1F, 0x38, 0x06, 0x24, 0xFE, 0xD8,
			0xC8, 0xC6, 0x40, 0xCB, 0x28, 0xB6, 0xD9, 0xED
		};
		vector<uint8_t> plaintext192 = {
			0x5A, 0x3B, 0xDC, 0xED, 0xAF, 0x03, 0xF5, 0xA2,
			0xA9, 0x82, 0x3A, 0xAB, 0x33, 0x88, 0xD0, 0xB1
		};
		vector<uint8_t> plaintext256 = {
			0x95, 0x1C, 0x9C, 0xC4, 0x80, 0xFC, 0x2F, 0xB2,
			0x81, 0x03, 0xA6, 0x5C, 0x15, 0xEA, 0x10, 0xEC
		};
		t.setPlaintext128(plaintext128);
		t.setPlaintext192(plaintext192);
		t.setPlaintext256(plaintext256);

		t.setCipher(new RC6());

		t.TestAll();
	}

}
